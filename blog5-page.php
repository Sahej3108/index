                    <h3>What is ZHFR / LSHF 546 UV Compound?</h3>
                    <p>
                        ZHFR-546 UV is a high-performance Zero Halogen Flame Retardant (ZHFR) / Low Smoke Halogen Free (LSHF) compound developed by Takkar Polychem Private Limited.
                    </p>

                    <p>
                        It is specifically designed for cable manufacturers who require consistent processing, low smoke emission, and long-term durability under harsh environmental conditions.
                    </p>

                    <ul>
                        <li>Zero halogen composition</li>
                        <li>Low smoke emission</li>
                        <li>UV resistant for outdoor applications</li>
                        <li>Stable mechanical properties</li>
                        <li>Optimized for smooth extrusion</li>
                    </ul>

